    /**
     * RSA公钥加密
     *
     * @param str 加密字符串
     *            公钥
     * @return 密文
     */

    public static String encrypt(String value) {
        value = new StringBuilder(value).reverse().toString();
        BigInteger valueInt = hexToBigInteger(stringToHex(value));
        return valueInt.modPow(pubkey, modulus).toString(16);

    }

    private static BigInteger hexToBigInteger(String hex) {
        return new BigInteger(hex, 16);
    }

    private static String stringToHex(String text) {
        return DatatypeConverter.printHexBinary(text.getBytes(StandardCharsets.UTF_8));
    }

    /**
     * 得到公钥
     *
     * @param key 密钥字符串（经过base64编码）
     * @throws Exception
     */
    public static PublicKey getPublicKey(String key) throws Exception {
        X509EncodedKeySpec keySpec = new X509EncodedKeySpec(
                Base64.decodeBase64(key.getBytes()));
        KeyFactory keyFactory = KeyFactory.getInstance("RSA");
        PublicKey publicKey = keyFactory.generatePublic(keySpec);
        return publicKey;
    }

